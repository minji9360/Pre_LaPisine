#include <stdio.h>

int main_2753(void) {
	int year;
	scanf_s("%d", &year);

	if ((year % 4 == 0 && year % 100 != 0) || year % 400 == 0) {
		printf("1");
	}
	else {
		printf("0");
	}
}

// 몇 번 틀린 이유
// 1. 윤년은 연도가 "4의 배수이면서, 100의 배수가 아닐 때" 또는 "400의 배수일 때"이다.
//    이 부분을 "4의 배수"이면서, "100의 배수가 아닐 때 또는 400의 배수일 때" 로 읽었다.
//    문제를 잘못 읽은건데, 조금만 주의깊게 읽고 예시를 읽어봤으면 바로 알았을 부분이다.
// 2. 오타났다. 백준에서는 scanf_s 안쓰고 scanf를 써야한다. 그리고 400을 300이라고 오타냈다.